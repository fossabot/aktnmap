<template>
  <div>
    <q-list link no-border>
      <!--
        Edit link
      -->
      <q-item id="edit-settings" @click="editSettings">
        <q-item-section icon="settings" />
        <q-item-label>{{$t('sideNav.SETTINGS')}}</q-item-label>
      </q-item>
    </q-list>
    <!--
      Create editor
     -->
    <k-modal-editor
      id="editor"
      ref="editor" 
      service="settings"
      objectId="settings"
      @applied="onSettingsEdited" />
  </div>
</template>

<script>

export default {
  name: 'settings',
  inject: ['sideNav'],
  methods: {
    editSettings () {
      this.sideNav.layout.hideCurrentSide(() => this.$refs.editor.open())
    },
    onSettingsEdited () {
      this.$refs.editor.close()
    }
  },
  created () {
    // Load the required components
    this.$options.components['k-modal-editor'] = this.$load('editor/KModalEditor')
  }
}
</script>